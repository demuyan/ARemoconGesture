	public void setSkinHistgram(PreviewData previewData, Rect rect){

		int width = rect.bottom - rect.top + 1;
		int height = rect.right - rect.left + 1;
		ByteBuffer bb = ByteBuffer.allocateDirect(width * height * 4);

		int i = 0;
		for (int y = rect.top; y <= rect.bottom; y++){
			for (int x = rect.left; x <= rect.right; x++){
				
				int pos = (x+y*previewData.width)*2;
				short col0 = (short)previewData.srcImage[pos+0];
				short col1 = (short)previewData.srcImage[pos+1];
				short color = (short)((col0 << 8) | col1);
				
				bb.put(i, (byte)0xff); i++; // A
				bb.put(i, (byte)((color >> (5+6)) & 0x1f) ); i++; // R
				bb.put(i, (byte)((color >> 5) & 0x3f) ); i++; // G
				bb.put(i, (byte)((color >> 0) & 0x1f) ); i++; // B
			}
		}
		setSkinHistgram(bb, width, height);
	}
	
	public void setSkinHistgram(Bitmap bitmap){

		byte[] photoData = null;
		int width = bitmap.getWidth();
		int height = bitmap.getHeight();

		int pixels[] = new int[width];
		
		ByteBuffer bb = ByteBuffer.allocateDirect(width * height * 4);
		int i = 0;
		for (int y = 0; y < height; y++){
			bitmap.getPixels(pixels, 0, width, 0, y, width, 1);
			for (int x = 0; x < width; x++){
				int color = pixels[x];
				bb.put(i, (byte)((color >> 24) & 0xff) ); i++;
				bb.put(i, (byte)((color >> 16) & 0xff) ); i++;
				bb.put(i, (byte)((color >> 8) & 0xff) ); i++;
				bb.put(i, (byte)((color >> 0) & 0xff) ); i++;
			}
		}
//		bitmap.copyPixelsToBuffer(bb);
		setSkinHistgram(bb, bitmap.getWidth(), bitmap.getHeight());
	}
	
	
