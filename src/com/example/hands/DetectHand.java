	private double getDistance(double[] feat1, double[] feat2)
	{
		double dist = 0.0;
		for(int i=0; i<feat1.length; i++){
			double val = Math.abs(feat1[i] - feat2[i]);
			dist += val * val;
		}
		return Math.sqrt(dist);
	}
	
